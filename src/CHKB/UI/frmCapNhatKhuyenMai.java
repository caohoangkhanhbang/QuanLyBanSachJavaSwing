    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        try {
            if(kiemTraRong() == false) {
                ThongBao tb = new ThongBao(this);
                tb.showMessage("Thông báo!", "Vui lòng nhập đúng và đầy đủ nội dung để thêm!");
            } else {
                String tenCT = txt_tenChuongTrinh.getText().trim();
                String giam = txt_giam.getText().trim();
                String ngayKT = txt_ngayKT.getText().trim();
                String ngayBD = txt_ngayBD.getText().trim();
                
                String trangThai = "0";
                if (ckb_hienThi.isSelected()) {
                    trangThai = "1";
                }
                String sql = "set dateformat dmy update KhuyenMai set TenChuongTrinh = N'"+tenCT+"', NgayBatDau = '"+ngayBD+"', NgayKetThuc = '"+ngayKT+"', TrangThai = '"+trangThai+"' where MaKhuyenMai = "+ maKhuyenMai;
                ConnectionToDatabase con = new ConnectionToDatabase();
                try {
                    if (con.ThemXoaSua(sql) >= 1) {
                        ThongBao tb = new ThongBao(this);
                        tb.showMessageSetIcon("Thông báo!", "Cập nhật thành công!", "/CHKB_IMAGE/successful.png");
                        this.dispose();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmCapNhatKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(frmCapNhatKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_themActionPerformed

