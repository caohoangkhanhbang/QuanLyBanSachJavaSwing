    public boolean kiemTraRong() throws ParseException
    {
        String tenCT = txt_tenChuongTrinh.getText().trim();
        String giam = txt_giam.getText().trim();
        
//        Date ngayBatDau = new SimpleDateFormat("dd/MM/yyyy").parse(txt_ngayBD.getText().trim());
//        Date ngayKetThuc = new SimpleDateFormat("dd/MM/yyyy").parse(txt_ngayKT.getText().trim());
//        String ngayBD = new SimpleDateFormat("dd").format(ngayBatDau);
//        String ngayKT = new SimpleDateFormat("dd").format(ngayKetThuc);
//        String thangBD = new SimpleDateFormat("MM").format(ngayBatDau);
//        String thangKT = new SimpleDateFormat("MM").format(ngayKetThuc);
//        String namBD = new SimpleDateFormat("yyyy").format(ngayBatDau);
//        String namKT = new SimpleDateFormat("yyyy").format(ngayKetThuc);
        
        boolean kq = true;
        if(tenCT.length()<1 || giam.length() < 1 )
        {
            kq = false;
        }
        if (con.kiemTraSo(giam) == false) {
            ThongBao tb = new ThongBao(this);
            tb.showMessage("Thông báo!", "Phần trăm giảm phải là số và lớn hơn 0");
            kq = false;
        }
        
//        String dateString = "2024-01-20";
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate localDate = LocalDate.parse(txt_ngayBD.getText(), formatter);
        LocalDate localDate2 = LocalDate.parse(txt_ngayKT.getText(), formatter);
        long daysBetween = ChronoUnit.DAYS.between(localDate, localDate2);
        
//        JOptionPane.showConfirmDialog(null, daysBetween+"");
        if(daysBetween<0)
        {
            ThongBao tb = new ThongBao(this);
            tb.showMessage("Thông báo!", "Thời gian khuyến mãi không hợp lệ!\nVui lòng xem lại ngày bắt đầu và kết thúc phải lớn hơn hoặc bằng 1 ngày!!!");
            kq = false;
        }
        return kq;
    }
    
