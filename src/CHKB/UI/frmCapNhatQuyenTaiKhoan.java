    public void anhXa(NhanVien nv)
    {
        txt_tenDangNhap.setText(nv.getTenDangNhap());
        hinhSP.setIcon(new ImageIcon(new ImageIcon(nv.getHinh()).getImage().getScaledInstance(hinhSP.getWidth(), hinhSP.getHeight(), Image.SCALE_SMOOTH)));
        txt_maNhanVien.setText(nv.getMaNhanVien());
        txt_tenNhanVien.setText(nv.getHoTen());
    }

    public void loadQuyen() throws SQLException
    {
        String sql = "{call getAllQuyen}";
        ManipulationNhanVien getRole = new ManipulationNhanVien();
        lstQuyen = getRole.getRole(sql);
        for(String role: lstQuyen)
        {
            cbb_quyen.addItem(role);
        }
    }
    
