    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        ConnectionToDatabase con = new ConnectionToDatabase();
        String quyen = (String) cbb_quyen.getSelectedItem();
        String tenDangNhap = txt_tenDangNhap.getText();
        String maNhanVien = txt_maNhanVien.getText();
         MessageDialog tb = new MessageDialog(this);
            tb.showMessage("Cảnh báo", "Bạn có chắc phân quyền: "+ quyen + " cho tài khoản: "+ tenDangNhap, "Phân quyền", "Hủy");
            if (tb.getMessageType() == MessageDialog.MessageType.OK) {
                String sql = "update NhanVien set VaiTro = N'"+quyen+"' where TenDangNhap = N'"+tenDangNhap+"' and MaNhanVien = "+ Integer.parseInt(maNhanVien);
            try {
                if(con.ThemXoaSua(sql) >= 1)
                {
                    ThongBao t = new ThongBao(this);
                    t.showMessage("Thông báo", "Cập nhật quyền thành công!!!", "Thoát");
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmCapNhatQuyenTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
    }//GEN-LAST:event_btn_themActionPerformed

